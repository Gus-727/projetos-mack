num = int(input('Digite um número'))
num2 = int(input('Digite outro número'))
soma = num + num2
sub = num - num2
mult = num * num2
div = num/num2
divt = num//num2
resto = num % num2
exp = num ** num2
print(f'O resultado da soma é {soma}')
print(f'O resultado da subtração é {sub}')
print(f'O resultado da multiplicação é {mult}')
print(f'O resultado da divisão é {div}')
print(f'O resultado da divisão truncada é {divt}')
print(f'O resultado do resto da divisão é {resto}')
print(f'O resultado da exponenciação é {exp}')